def _candidate_paths() -> list[Path]:
    current_repo_path = Path(__file__).resolve().parent / "prompt_assets" / "ai_memory_v2.md"
    legacy_repo_path = (
        Path(__file__).resolve().parents[5]
        / "Resonnet"
        / "libs"
        / "profile_helper"
        / "prompts"
        / "ai-memory-v2.md"
    )
    return [current_repo_path, legacy_repo_path]


def load_ai_memory_prompt() -> str:
    for path in _candidate_paths():
        if path.exists() and path.is_file():
            return path.read_text(encoding="utf-8").strip()
    return _PROMPT_FALLBACK.strip()

